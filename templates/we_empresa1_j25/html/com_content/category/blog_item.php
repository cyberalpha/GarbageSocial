		'10' => JText::_('OCTOBER_SHORT'),
		'11' => JText::_('NOVEMBER_SHORT'),
		'12' => JText::_('DECEMBER_SHORT')
		);
		
?>

<?php if ($this->item->state == 0) : ?>
<div class="system-unpublished">
<?php endif; ?>
<?php if ($params->get('show_title')) : ?>
	
	<?php if ($params->get('show_create_date')) : ?>
			<div class="create">
			<?php $created= JHTML::_('date', $this->item->created, 'Y-m-d');

					$fecha = explode("-",$created);
					$createDay = $fecha[2];
					$createMonth = $mes[$fecha[1]];
					$createYear = $fecha[0];			
				?>
				<span class="fdia"><?php echo $createDay; ?></span>
				<div class="fmesanio">
				<span class="fmes"><?php echo $createMonth; ?></span>
				<span class="fanio"><?php echo $createYear; ?></span>
				</div>
